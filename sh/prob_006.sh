#!/bin/sh

(
    seq -s ' ' 1 100 \
    |
    sed -e 's/[0-9]*/&^2/g' \
    |
    tr ' ' '+' \
    |
    sed -e 's/.*/(&)/' \
    ;
    # 二乗の和の算出式の生成

    seq -s ' ' 1 100 \
    |
    sed -e 's/.*/(&)^2/' \
    |
    tr ' ' '+'
    # 和の二乗の算出式の生成
) \
|
awk 'BEGIN {RS=""} {print $2,"-",$1}' \
| # (二乗の和)と(和の二乗)の差を求める算出式の生成
bc
  # (二乗の和)と(和の二乗)の差の出力
